var (
	nounList      = strings.Split(Nouns, "\n")
	adjectiveList = strings.Split(Adjectives, "\n")
)

func GenerateThing() string {
	twoNum := rand.Intn(89) + 10
	noun := nounList[rand.Intn(len(nounList))]
	adjective1 := adjectiveList[rand.Intn(len(adjectiveList))]
	adjective2 := adjectiveList[rand.Intn(len(adjectiveList))]
	return fmt.Sprintf("%s%s%s%v", adjective1, adjective2, noun, twoNum)
}
